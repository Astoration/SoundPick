$('#DesignOpen').click(function(){
    if($('#DesignOpen+input').val()=='false'){
        $('#DesignOpen').css('color','#d2f1f0');
        $('#DesignOpen').css('border-bottom','#d2f1f0 solid 1px');
        $('#DesignOpen+input').val('true');
    }else{
        $('#DesignOpen').css('color','#c9c9c9');
        $('#DesignOpen').css('border-bottom','#c9c9c9 solid 1px');
        $('#DesignOpen+input').val('false');
    }
});
